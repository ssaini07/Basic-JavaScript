//for loop
let sum = 0;
const n = 10;

for (let i = 1; i <= n; i++) {
    sum += i;
}
console.log('Sum is: ' + sum);

//while loop
let count = 1;
while (count <= 10) {
    console.log('Count is: ' + count);
    count += 1;
}

//do while loop
let count1 = 1;
do {
    console.log('Count in do while: ' + count1);
    count1 += 1;
} while (count1 <= 10);