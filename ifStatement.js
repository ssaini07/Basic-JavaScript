//If statement
var x = 50;
if (x > 20) {
    console.log(x);
}

// if else statement
var y = 100;
if (y % 10 == 0) {
    console.log('y is even');
} else {
    console.log('y is not even');
}

//if ..elseif statement
var z = 20;
if (z == 10) {
    console.log('z is equal to 10');
} else if (z == 20) {
    console.log('z is equal to 20');
} else {
    console.log('z is neither equal to 10 or 20');
}