//switch statement
const grade = 'D';
switch (grade) {
    case 'A':
        console.log('Student got A grade');
        break;
    case 'B':
        console.log('Student got B grade');
        break;
    case 'C':
        console.log('Student got C grade');
        break;

    default:
        console.log('Student did not appear for exam');
        break;
}